package Ch03;

public class Ex03_09 {
	public static void main(String[] args) {
		
		float a = 12345678901234567890.0f;
		double b = 12345678901234567890.0;
		System.out.println("float 변수 a의 값은 : " + a);
		System.out.println("float 변수 b의 값은 : " + b);
		float c = 1.0f / 3.0f;
		double d = 1.0 / 3.0;
		System.out.println("float 변수 c의 값은 : " + c);
		System.out.println("double 변수 d의 값은 : " + d);
	}
}
